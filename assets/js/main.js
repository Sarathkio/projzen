// Search filter for the table (works on all columns)
document.addEventListener("DOMContentLoaded", function () {
  const searchInput = document.getElementById('projectSearch');
  const table = document.getElementById('projectsTable');

  if (searchInput && table) {
    searchInput.addEventListener('input', function () {
      const filter = this.value.toLowerCase();

      if (table.tBodies.length > 0) {
        const rows = table.tBodies[0].rows;

        for (let i = 0; i < rows.length; i++) {
          let rowText = rows[i].textContent.toLowerCase();
          rows[i].style.display = rowText.includes(filter) ? '' : 'none';
        }
      }
    });
  } 
});


document.addEventListener('DOMContentLoaded', function () {
  const form = document.querySelector('.php-email-form');

  form.addEventListener('submit', function (e) {
    e.preventDefault(); // Prevent default form submission

    const submitBtn = form.querySelector('.submit-btn span');
    const loading = form.querySelector('.loading');
    const errorMsg = form.querySelector('.error-message');
    const sentMsg = form.querySelector('.sent-message');

    // Show loading
    loading.style.display = 'block';
    errorMsg.style.display = 'none';
    sentMsg.style.display = 'none';

    // Send email using EmailJS
    emailjs.sendForm('service_nge6w3i', 'template_3ie528t', this)
      .then(() => {
        loading.style.display = 'none';
        sentMsg.style.display = 'block';
        form.reset();
      }, (err) => {
        loading.style.display = 'none';
        errorMsg.style.display = 'block';
        errorMsg.innerText = 'Something went wrong. Please try again later.';
        console.error('EmailJS Error:', err);
      });
  });
});

